package com.tnsif.dayone;

public class CharDemo {

    public static void main(String[] args) {
        char ch = 'A';
        char ch1 = 66; // ASCII value of 'B'
        int var1 = ch;
        int var2 = ch1;

        System.out.println(ch);    // prints: A
        System.out.println(ch1);   // prints: B
        System.out.println(var1);  // prints: 65
        System.out.println(var2);  // prints: 66
    }
}
